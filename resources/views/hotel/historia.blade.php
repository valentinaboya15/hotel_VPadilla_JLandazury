<h2>HISTORIA</h2>

<h4>El Complejo Hotelero RIVERA tiene sus inicios hacia el año 1.988, siendo pionero en el sector,
 establecido en la misma ubicación actual y con el nombre de “Hotel Cabañas rivera”; donde inicialmente se da apertura a un restaurante de comida típica Colombiana, 
 concurrido por diferentes sectores de la población Tumaqueña, que poco a poco se fue ganando el reconocimiento de propios y extraños gracias a la oferta gastronómica que propuso.  
 Poco tiempo después, se construyen 2 módulos de 2 pisos para alojamiento: Uno de ellos dividido en 2 habitaciones para grupos de hasta 12 personas cada una; y el otro, 
 dividido en 4 habitaciones para familias o grupos de hasta 4 personas, contando con una disponibilidad máxima para 40 personas.

Hacia el año 2.005 se concibe la idea de crecer y fortalecerse.  Tras un análisis profundo y asumiendo retos gigantescos como el estar en una región olvidada y desconocida como sitio turístico, 
pero con el sueño de verla convertida en un destino consolidado y conociendo el gran potencial paisajístico, gastronómico, cultural y biodiverso, se toma la decisión de construir el nuevo Complejo Hotelero,
 apuntando a altos estándares de calidad y que fuera capaz de estar a la altura de los mejores hoteles de otras partes del país.

Pensando también en el progreso de la región, el compromiso social y apostándole al nacimiento de un nuevo destino de renombre en Colombia, abre sus puertas al público en el mes de Marzo de 2009  el COMPLEJO HOTELERO RIVERA;
 el cual ha sido un referente que ha contribuido a impactar de manera positiva la imagen del municipio y el departamento.
</h4>
